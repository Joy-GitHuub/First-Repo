// JavaScript Switch Break 

console.log('" JavaScript Switch Break Concept"');

// Switch Almost if else faster version.

const color = 'red';

switch (color) {
    case 'Blue':
        console.log('Color is Blue');
        break;
    case 'white':
        console.log('Color is White');
        break;
    case 'yellow':
        console.log('Color is Yellow');
        break;
    case 'green':
        console.log('Color is Green');
        break;
    case 'red':
        console.log('Color is Red');
        break;
    default:
        console.log('Color is Black');
};