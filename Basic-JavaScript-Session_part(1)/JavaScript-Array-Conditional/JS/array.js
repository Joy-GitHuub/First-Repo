// JavaScript Array 

console.log('JavaScript Array Declear');


/*
An array is a special variable, which can hold more than one value
*/

// It is a common practice to declare arrays with the const keyword.


// Hold all firends Age Just 1 variable.
const friendsAge = [12, 11, 13, 14, 17, 19];
console.log('friends Age Array', friendsAge);

// Hold some Cars names Just 1 variable.
const cars = ["Saab", "Volvo", "BMW"];
console.log('cars Name Array', cars);

// Hold all Vowels Just 1 Variable
const vowels = ['a', 'e', 'i', 'o', 'u'];
console.log('vowels Array', vowels);




/* 
The length Property
The length property of an array returns the length of an array (the number of array elements).

Array Total Elements Check
*/

console.log('Friends Age Array Length', friendsAge.length);
console.log('Cars Array Length', cars.length);
console.log('Vowels Array Length', vowels.length);