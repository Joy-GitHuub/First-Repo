console.log('"JavaScript Comparison Operator"');

const x = 34;
const y = 53;
// Comparison Operators..
console.log('greater than', x > y);
console.log('less than', x < y);
console.log('Equal', x == y);
console.log('Not Equal', x != y);


// Advance Comparison Operators..
const a = 5;
const b = '5';
console.log('greater than or equal to', a >= b);
console.log('less than or equal to', a <= b);
console.log('equal value and equal type check', a === b);
console.log('not equal value or not equal type', a !== b);